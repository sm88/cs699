#ifndef _MYSORT_H_
#define _MYSORT_H_

/**
  *Uses quicksort algorithm to sort a list of integers
  *Argument: pointer to list, start index, end index
  *Return: void
**/
void quickSort(int *aList, int aStart, int aEnd);

/**
  *Uses insertionsort algorithm to sort a list of integers
  *Argument: pointer to list, start index, end index
  *Return: void
**/
void insertionSort(int *aList, int aStart, int aEnd);

/**Threshold for invoking insertionSort in quickSort**/
const int INSERTION_SORT_THRESHOLD = 10;

/**Maximum length of list**/
const int MAX_LENGTH = 10000;

/**insertion sort limit**/
const int INSERTION_SORT_LIMIT = 100;
#endif
